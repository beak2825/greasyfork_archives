    function Translate(html, word) {
        if (!html || !word) return html;
        if (html.trim() == "") return html;
        for (let i = 0; i < word.length / 2; ++ i) {
            if (!word[i*2] || !word[i*2+1]) continue;
            html = html.replace(new RegExp(word[i*2], 'ig'), word[i*2+1]);
        }
        return html;
    }
