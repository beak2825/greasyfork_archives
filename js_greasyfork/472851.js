const userIdInAvatarUrlRegex = /\/avatars\/(\d+)\//;
const channelIdRegex = /\/channels\/(\d+)\//;

function extractDiscordId() {
    const match = location.href.match(channelIdRegex)
    if (!match || match.length < 2) {
        console.log("r/poland", "Nie znaleziono kanału w url, pomijanie...");
    } else {
        return match[1];
    }
}

window.addEventListener('locationchange', function () {
    console.log("r/poland", 'location changed!');
});

const monitor = new MutationObserver(markElements);

function markElements() {
    const elements = document.querySelectorAll('[data-list-item-id^="members"]');
    console.log("r/poland", `Znalezniono ${elements.length} pozycji`);

    elements.forEach((parentElement) => {
        const avatarUrl = parentElement.querySelector('img').src
        const match = avatarUrl.match(userIdInAvatarUrlRegex);

        if (!match || match.length < 2) {
            console.log("r/poland", `Niespodziewany błąd: url użytkownika nie ma id: ${avatarUrl}`);
        } else {
            const userId = match[1];
            if (ids.has(userId)) {
                console.log("r/poland", `Znaleziono usera z list o id=${userId}. Podmienianie podpisu.`);
                const underUserElement = parentElement.querySelector('[class^="subText"]');
                underUserElement.innerHTML = `<div style="font-weight: bold; color: red;">Ex r/poland</div>`
            }
        }
    });
}


async function lookForMembersTab() {
    await new Promise(r => setTimeout(r, 5000))
        .then(() => {
            const element = document.querySelector('[aria-label="Członkowie"]');
            if (element === null) {
                console.log("r/poland", "Nie znaleziono zakładki z użytkownikami, ponowna próba za 5 sekund");
                lookForMembersTab();
            } else {
                console.log("r/poland", "Znaleziono zakładkę, podpinanie obserwera");
                monitor.observe(element, {
                    attributes: false,
                    childList: true,
                    characterData: true
                });
                markElements();
            }
        })
}

