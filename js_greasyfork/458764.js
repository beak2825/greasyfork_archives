/**
 * Looks up the name of an airport code in the airportNames object or the layover cities checkbox list
 *
 * @param code The 3-letter airport code
 * @returns The name of the airport
 */
function lookupCode(code) {
    // check if the code is in the airportNames object
    let name = airportNames[code];
    if (!name) {
        // otherwise, get the city from the layover cities checkbox list
        name = $(`input[data-name="${code}"] ~ span:first-of-type`)
            .text()
            .replace(/\(.*\)/, "")
            .trim();
    }
    return name;
}

/**
 * Adds a "Show Times" checkbox to the "Filters" menu
 */
function addShowTimesCheckbox() {
    // force opacity to 1 on "#trip-list-wrapper .trip-path-point-airport-demp .trip-path-point-time" elements
    // if "Always Show Times" is checked
    $(".filters-container__scroll-body").append(
        `<div class="flights-tracker show-times"><span>Always Show Times</span><label class="skip-toggle"><span class="skip-toggle__label">Off</span><input type="checkbox" class="skip-toggle__input" name="track" value="false" id="show-times"><div class="skip-toggle__switch"></div></label></div>`
    );
    // add a listener for the checkbox to show/hide the times
    $("#show-times").change(function () {
        showOrHideTimes(this.checked);
    });
}

/**
 * If the checkbox is checked, show the times, otherwise hide them
 *
 * @param {boolean} checked Whether the checkbox is checked
 */
function showOrHideTimes(checked) {
    if (checked) {
        $("#trip-list-wrapper .trip-path-point-airport-demp .trip-path-point-time").css("opacity", 1);
        $("div.show-times > label > span.skip-toggle__label").text("On");
    } else {
        $("#trip-list-wrapper .trip-path-point-airport-demp .trip-path-point-time").css("opacity", "");
        $("div.show-times > label > span.skip-toggle__label").text("Off");
    }
}

/**
 * Enables all tooltips on the page
 */
function enableTooltips() {
	$('[data-toggle="tooltip"]').tooltip();
}

