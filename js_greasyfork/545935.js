let elementVarsMap = new Map();

function clearOverlays() {
    const existingOverlays = document.querySelectorAll('.css-var-overlay');
    existingOverlays.forEach(overlay => overlay.remove());
}

function drawOverlays(filterText = '') {
    clearOverlays();
    const lowerCaseFilter = filterText.toLowerCase().trim();

    elementVarsMap.forEach((varNames, element) => {
        const allVarNames = Array.from(varNames);
        const filteredVarNames = lowerCaseFilter === ''
            ? allVarNames
            : allVarNames.filter(name => name.toLowerCase().includes(lowerCaseFilter));

        if (filteredVarNames.length === 0) {
            return;
        }

        const rect = element.getBoundingClientRect();
        if (rect.width === 0 || rect.height === 0 || element.classList.contains('css-var-overlay')) return;

        const overlay = document.createElement('div');
        overlay.className = 'css-var-overlay';
        overlay.style.position = 'absolute';
        overlay.style.top = `${rect.top + window.scrollY}px`;
        overlay.style.left = `${rect.left + window.scrollX}px`;
        overlay.style.width = `${rect.width}px`;
        overlay.style.height = `${rect.height}px`;
        overlay.style.border = '1px dashed #f5222d';
        overlay.style.backgroundColor = 'rgba(245, 34, 45, 0.1)';
        overlay.style.zIndex = '2000'; // High z-index to be on top
        overlay.style.pointerEvents = 'none';
        overlay.style.boxSizing = 'border-box';

        const varList = document.createElement('div');
        varList.style.position = 'absolute';
        varList.style.top = '0';
        varList.style.right = '0';
        varList.style.backgroundColor = 'rgba(0, 0, 0, 0.7)';
        varList.style.color = 'white';
        varList.style.padding = '2px 5px';
        varList.style.fontSize = '12px';
        varList.style.fontFamily = 'monospace';
        varList.style.lineHeight = '1.4';
        varList.style.wordBreak = 'break-all';
        varList.style.whiteSpace = 'nowrap'; // 1. Don't wrap lines
        varList.innerHTML = filteredVarNames.join('<br>');

        overlay.appendChild(varList);
        document.body.appendChild(overlay);

        // 2. Reposition if off-screen
        const listRect = varList.getBoundingClientRect();
        const viewportWidth = window.innerWidth;
        const viewportHeight = window.innerHeight;

        if (listRect.right > viewportWidth) {
            varList.style.right = 'auto';
            varList.style.left = '0';
        }

        if (listRect.left < 0) {
            varList.style.left = '0';
        }

        if (listRect.bottom > viewportHeight) {
            varList.style.top = 'auto';
            varList.style.bottom = '0';
        }

        if (listRect.top < 0) {
            varList.style.top = '0';
        }
    });
}


function runVariableAnalysis() {
    elementVarsMap = new Map();
    const varRegex = /var\((--[\w-]+)/g;

    const addVarsToMap = (element, cssValue) => {
        if (!cssValue) return;
        let match;
        varRegex.lastIndex = 0;
        while ((match = varRegex.exec(cssValue)) !== null) {
            if (!elementVarsMap.has(element)) {
                elementVarsMap.set(element, new Set());
            }
            elementVarsMap.get(element).add(match[1]);
        }
    };

    const findVarsInRules = (rules) => {
        if (!rules) return;
        Array.from(rules).forEach(rule => {
            if (rule.style) {
                let hasVar = false;
                for (let i = 0; i < rule.style.length; i++) {
                    const value = rule.style.getPropertyValue(rule.style[i]);
                    if (value && value.includes('var(')) {
                        hasVar = true;
                        break;
                    }
                }

                if (hasVar) {
                    try {
                        const elements = document.querySelectorAll(rule.selectorText);
                        elements.forEach(element => {
                            for (let i = 0; i < rule.style.length; i++) {
                                const prop = rule.style[i];
                                const value = rule.style.getPropertyValue(prop);
                                addVarsToMap(element, value);
                            }
                        });
                    } catch (e) {
                        // Ignore invalid selectors
                    }
                }
            } else if (rule.cssRules) {
                findVarsInRules(rule.cssRules);
            }
        });
    };

    Array.from(document.styleSheets).forEach(sheet => {
        try {
            findVarsInRules(sheet.cssRules || sheet.rules);
        } catch (e) {
            if (e.name !== 'SecurityError') {
                console.warn("Cannot access stylesheet rules:", sheet.href, e);
            }
        }
    });

    const allElements = document.querySelectorAll('*[style]');
    allElements.forEach(element => {
        for (let i = 0; i < element.style.length; i++) {
            const prop = element.style[i];
            const value = element.style.getPropertyValue(prop);
            addVarsToMap(element, value);
        }
    });

    if (elementVarsMap.size === 0) {
        console.log("No elements found using CSS variables.");
    }
}

