    function fetch(url, callback) {
        var xhr = new XMLHttpRequest();
        xhr.addEventListener('load', () => {
            callback(JSON.parse(xhr.responseText));
        });
        xhr.open('GET', url);
        xhr.setRequestHeader('accept', 'application/json');
        xhr.send();
    }

