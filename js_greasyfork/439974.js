class Topic {
    constructor(elTopic) {
        this.sTitle = '';
        this.elItems = null;

        this.init(elTopic);
    }

    init(elTopic) {
        this.sTitle = elTopic.querySelector('div.name').innerText.trim();
        this.elItems = elTopic.querySelectorAll('div.list > ul > li > label');
        this.exec();
    }

    exec() {
        debugger;
        let _sTitle = this.sTitle.replaceAll('（', '').replaceAll('）', '').replaceAll('。', '').replaceAll('？', '').replaceAll(' ', '');
        let index = -1;

        for(let i = 0; i < topics.length; i++) {
            let topic = topics[i].replaceAll('（', '').replaceAll('）', '').replaceAll('。', '').replaceAll('？', '').replaceAll(' ', '');

            if(_sTitle == topic) {
                index = i;
                break;
            }
        }
        if(index < 0) {
            console.log('未在题库中查找到题目: ' + this.sTitle);
            return;
        }
        for(let i = 0; i < this.elItems.length; i++) {
            let txt = this.elItems[i].innerText.replaceAll(' ', '').replaceAll('\n', '').slice(2);

            if(txt == answers[index]) {
                this.elItems[i].click();
            }
        }
    }
}

