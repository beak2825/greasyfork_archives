function changeNotation(notation) {
    let style = document.getElementById("font-style");
    if (notation == "ain-Kana" || notation == "ain-Hiragana") {
        style.textContent = '@font-face{font-family:"Mkana+";src:local("Mkana+"),url("https://toracatman.github.io/fonts/mkanaplus.woff2")format("woff2"),url("https://toracatman.github.io/fonts/mkanaplus.woff")format("woff");font-display:swap;}[data-roman],.mw-body-content [data-roman]{font-family:"Mkana+";}[data-exclude],.mw-body-content [data-exclude]{font-family:sans-serif;}';
    }
    else if (notation == "ain-Cyrl") {
        style.textContent = '[data-roman],.mw-body-content [data-roman]{font-family:"Arial";}[data-exclude],.mw-body-content [data-exclude]{font-family:sans-serif;}';
    }
    else {
        style.textContent = "";
    }

    let ain = document.querySelectorAll(".mw-parser-output :lang(ain)");
    if (ain.length == 0) return;
    if (notation == "roman") {
        for (let i = 0; i < ain.length; i++) {
            let roman = ain[i].getAttribute("data-roman");
            if (roman == null) continue;
            ain[i].textContent = roman;
            ain[i].removeAttribute("data-roman");
        }
    }
    else {
        for (let i = 0; i < ain.length; i++) {
            let roman = ain[i].getAttribute("data-roman");
            if (roman != null) continue;
            roman = ain[i].textContent;
            ain[i].setAttribute("data-roman", roman);
        }
        let s = ain[0].getAttribute("data-roman") ?? ain[0].textContent;
        for (let i = 1; i < ain.length; i++) {
            s += "\x1e" + (ain[i].getAttribute("data-roman") ?? ain[i].textContent);
        }
        let p = [];
        s = s.replace(/[A-Z]{2,}|[A-Z]\d|[0-9‚Üä-‚ÜèÔï¨-ÔïØ]+(?=p|pe|n|iw)|[0-9‚Üä-‚ÜèÔï¨-ÔïØ]+[A-Za-z]+|\.{2,}/g, (m) => {
            p.push(m);
            return "\x1a";
        });
        for (let i = 0; i < c[notation].c.length; i++) {
            s = s.replace(new RegExp(c[notation].c[i][0], c[notation].f), c[notation].c[i][1]);
        }
        let ss = s.split("\x1a");
        s = "";
        for (let i = 0; i < p.length; i++) {
            s += ss[i] + p[i];
        }
        s += ss[p.length];
        s = s.split("\x1e");
        for (let i = 0; i < ain.length; i++) {
            ain[i].textContent = s[i];
        }
    }
}

