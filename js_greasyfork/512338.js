    // Function to add buttons to rows
    function addModalButton() {
        console.log('Checking for rows to add buttons...');
        const rows = document.querySelectorAll('table.wb tr');

        if (rows.length === 0) {
            console.log('No rows found yet.');
        }

        rows.forEach(row => {
            if (!row.querySelector('.creature-modal-button')) {
                const hintDiv = row.querySelector('div.cre_mon_parent[hint]');
                if (hintDiv) {
                    const hintName = hintDiv.getAttribute('hint');
                    console.log(`Found a creature with hint: ${hintName}`);

                    const creature = creatures.find(c => c.title === hintName);

                    if (creature && creature.level !== 5) {
                        console.log(`Adding button for ${hintName}`);
                        const lastCell = row.lastElementChild;
                        if (lastCell) {
                            const button = document.createElement('button');
                            button.textContent = '?';
                            button.className = 'creature-modal-button';
                            button.style.width = '30px';
                            button.style.height = '30px';
                            button.style.borderRadius = '50%';
                            button.style.border = 'none';
                            button.style.color = '#592c08';
                            button.style.fontFamily = 'verdana, geneva, arial cyr';
                            button.style.fontWeight = '700';
                            button.style.backgroundImage = 'url(https://dcdn.heroeswm.ru/i/homeico/art_btn_bg_gold.png)';
                            button.style.backgroundColor = '#dab761';
                            button.style.boxShadow = 'inset 0 0 0 1px #fce6b0, inset 0 0 0 2px #a78750, 0 0 0 1px rgba(0, 0, 0, .13)';
                            button.style.cursor = 'pointer';
                            button.style.marginLeft = '10px';

                            button.addEventListener('click', function() {
                                openModal(hintName);
                            });

                            lastCell.appendChild(button);
                        }
                    } else {
                        console.log(`No matching creature found or creature level is 5: ${hintName}`);
                    }
                }
            }
        });
    }

    // Function to open modal
    function openModal(hintName) {
        const creature = creatures.find(creature => creature.title === hintName);
        if (creature) {
            console.log(`Opening modal for: ${hintName}`);

            let filteredCreatures = [];

            if (creature.level === 4) {
                filteredCreatures = creatures.filter(c => c.level === 5);
            } else if (creature.level === 1 || creature.level === 2 || creature.level === 3) {
                filteredCreatures = creatures.filter(c => c.level === creature.level + 1 && c.fraction === creature.fraction);
            }

            const modal = document.createElement('div');
            modal.style.position = 'fixed';
            modal.style.top = '50%';
            modal.style.left = '50%';
            modal.style.transform = 'translate(-50%, -50%)';
            modal.style.backgroundColor = 'white';
            modal.style.border = '1px solid black';
            modal.style.padding = '20px';
            modal.style.zIndex = '1000';

            const overlay = document.createElement('div');
            overlay.style.position = 'fixed';
            overlay.style.top = 0;
            overlay.style.left = 0;
            overlay.style.width = '100%';
            overlay.style.height = '100%';
            overlay.style.backgroundColor = 'rgba(0, 0, 0, 0.5)';
            overlay.style.zIndex = '999';

            overlay.addEventListener('click', function() {
                document.body.removeChild(modal);
                document.body.removeChild(overlay);
            });

            const content = document.createElement('p');
            content.innerHTML = `<h1>Можно получить</h1>`;
            modal.appendChild(content);

            const flexContainer = document.createElement('div');
            flexContainer.style.display = 'flex';
            flexContainer.style.flexWrap = 'wrap';

            filteredCreatures.forEach(creature => {
                const creatureDiv = document.createElement('div');
                creatureDiv.classList.add('cre_creature');

                const creatureContent = `
                    <div class="cre_mon_parent">
                        <a title="${creature.title}" href="${creature.link}">
                            <img src="${creature.background}" width="45" height="37" class="cre_mon_image2">
                            <img src="${creature.picture}" width="45" height="37" class="cre_mon_image1">
                        </a>
                    </div>
                `;
                creatureDiv.innerHTML = creatureContent;

                flexContainer.appendChild(creatureDiv);
            });

            modal.appendChild(flexContainer);
            document.body.appendChild(overlay);
            document.body.appendChild(modal);
        }
    }

    // Retry every 500ms until the table is found
    function retryUntilTableLoaded() {
        const interval = setInterval(() => {
            const table = document.querySelector('table.wb');
            if (table) {
                console.log('Table found, adding buttons...');
                clearInterval(interval);  // Stop retrying
                addModalButton();    // Add buttons to existing rows
                observeTableChanges(); // Observe for new rows dynamically
            } else {
                console.log('Table not found yet, retrying...');
            }
        }, 500);  // Retry every 500ms
    }

    // MutationObserver to add buttons to newly added rows
    function observeTableChanges() {
        const targetNode = document.querySelector('table.wb');
        const config = { childList: true, subtree: true };

        const callback = function(mutationsList, observer) {
            console.log('Mutation detected, rechecking rows...');
            addModalButton();  // Add button to any new rows
        };

        const observer = new MutationObserver(callback);
        if (targetNode) {
            console.log('Starting to observe table changes.');
            observer.observe(targetNode, config);
        } else {
            console.log('Table not found for observation.');
        }
    }

     retryUntilTableLoaded();

