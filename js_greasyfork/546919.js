  function pickRandom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
  }

function injectRandomStyle(slotIndex) {
  const slot = AD_SLOTS[slotIndex];
 // eslint-disable-next-line no-multi-spaces
const id   = `tm-random-ad-${slotIndex}`;

  if (document.getElementById(id)) return;

  const url = pickRandom(slot.urls);
  const css = `
    ${slot.selector} {
      background-image: url('${url}') !important;
      background-size: contain !important;
      background-repeat: no-repeat !important;
      cursor: pointer !important;
    }
  `;
  const style = document.createElement('style');
  style.id = id;
  style.textContent = css;
  document.head.appendChild(style);

  console.log(`[RandomAd] Injected slot #${slotIndex}  ${url}`);
}

// skip the polling entirely:
AD_SLOTS.forEach((_, i) => injectRandomStyle(i));


function initAllAds() {
  let allDone = true;
  for (let i = 0; i < AD_SLOTS.length; i++) {
    const baseSelector = AD_SLOTS[i].selector.replace(/::(before|after)/g, '');
    const el = document.querySelector(baseSelector);
    if (el) {
      injectRandomStyle(i);
    } else {
      console.warn(`[RandomAd] Slot #${i} (${baseSelector}) not found yet`);
      allDone = false;
    }
  }
  return allDone;
}


