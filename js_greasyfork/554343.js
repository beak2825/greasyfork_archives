    /**
   * ============================================================
   * 通配符预编译系统
   * ============================================================
   */

    // 预编译正则缓存
    const wildcardRegexCache = new Map();

    /**
   * 编译通配符模式为可执行函数
   */
    function compileWildcardPattern(pattern) {
        if (pattern === '*') {
            return { type: 'any', test: () => true };
        }

        if (!wildcardRegexCache.has(pattern)) {
            // 转义正则特殊字符，但保留*
            const regexPattern = pattern
            .replace(/[.+?^${}()|[\]\\]/g, '\\$&') // 转义除了*以外的特殊字符
            .replace(/\*/g, '.*'); // 将*转换为.*

            const regex = new RegExp(`^${regexPattern}$`);
            wildcardRegexCache.set(pattern, { type: 'regex', test: (str) => regex.test(str) });
        }

        return wildcardRegexCache.get(pattern);
    }

    /**
   * 预编译多义词规则中的通配符模式
   */
    function precompileMultiWordRules(dict) {
        if (!dict.multiWord) return;

        for (const [targetWord, rules] of Object.entries(dict.multiWord)) {
            // 确保 rules 是数组格式
            const rulesArray = Array.isArray(rules) ? rules : [rules];

            // 更新回原对象，确保后续处理使用数组格式
            dict.multiWord[targetWord] = rulesArray;

            for (const rule of rulesArray) {
                // 预编译前词表模式
                if (rule.prefixSet) {
                    rule.compiledPrefixSet = rule.prefixSet.map(compileWildcardPattern);
                }
                // 预编译后词表模式
                if (rule.suffixSet) {
                    rule.compiledSuffixSet = rule.suffixSet.map(compileWildcardPattern);
                }
            }
        }
    }

    /**
   * ============================================================
   * 核心翻译引擎
   * ============================================================
   */

    /**
   * 从data-name中安全还原tagKey
   */
    function decodeDataName(raw) {
        if (!raw || typeof raw !== 'string') return raw;
        try {
            return decodeURIComponent(raw);
        } catch (e) {
            return raw.replace(/%2F/g, '/');
        }
    }

    /**
   * 检查多义词匹配条件（使用预编译模式）
   */
    function checkMultiWordCondition(beforeStr, afterStr, rule) {
        const hasPrefixSet = rule.compiledPrefixSet && rule.compiledPrefixSet.length > 0;
        const hasSuffixSet = rule.compiledSuffixSet && rule.compiledSuffixSet.length > 0;

        // 检查前词条件
        let prefixCondition = true;
        if (hasPrefixSet) {
            // 当有前词表时，要求前词必须存在且匹配
            prefixCondition = beforeStr !== '' && rule.compiledPrefixSet.some(compiledPattern =>
                                                                              compiledPattern.test(beforeStr)
                                                                             );
        } else {
            // 如果没有prefixSet，则要求前词为空
            prefixCondition = beforeStr === '';
        }

        // 检查后词条件
        let suffixCondition = true;
        if (hasSuffixSet) {
            // 当有后词表时，要求后词必须存在且匹配
            suffixCondition = afterStr !== '' && rule.compiledSuffixSet.some(compiledPattern =>
                                                                             compiledPattern.test(afterStr)
                                                                            );
        } else {
            // 如果没有suffixSet，则要求后词为空
            suffixCondition = afterStr === '';
        }

        return prefixCondition && suffixCondition;
    }

    /**
   * 应用多义词规则
   */
    function applyMultiWordRule(beforeStr, afterStr, rule) {
        // 应用翻译模板
        let translated = rule.template;
        translated = translated.replace(/{前词}/g, beforeStr);
        translated = translated.replace(/{后词}/g, afterStr);
        return translated;
    }

    /**
   * 单元素翻译函数（核心）
   */
    function translateOne(el, category) {
        if (!el || !(el instanceof Element)) return;

        // 获取原始tagKey（优先从data-name）
        let originalTag = null;
        const li = el.closest && el.closest('.tag-list-item');
        if (li && li.getAttribute) {
            const raw = li.getAttribute('data-name');
            if (raw) {
                originalTag = decodeDataName(raw);
            }
        }

        // 回退：使用显示文本
        if (!originalTag) {
            const txt = (el.textContent || '').trim();
            if (!txt) return;
            originalTag = txt;
        }

        // 黑名单检查（全局）
        if (blacklist.includes(originalTag)) {
            return;
        }

        // 获取分类词表
        const dict = categoryDicts[category];
        if (!dict) return;

        // 1) 完整匹配检查
        if (dict.complete && Object.prototype.hasOwnProperty.call(dict.complete, originalTag)) {
            const zh = dict.complete[originalTag];
            if (typeof zh === 'string' && zh.length > 0) {
                el.textContent = zh;
            }
            return;
        }

        let currentText = originalTag;
        let didChange = false;

        // 2) 短语匹配
        if (dict.phrase) {
            for (const [phrase, translation] of Object.entries(dict.phrase)) {
                if (currentText.includes(phrase)) {
                    // 只替换第一个匹配的短语
                    currentText = currentText.replace(phrase, translation);
                    didChange = true;
                    break; // 只匹配一次短语
                }
            }
        }

        // 3) 逐词匹配
        if (dict.word) {
            const parts = currentText.split(/([_\/])/);
            let wordChanged = false;

            for (let i = 0; i < parts.length; i++) {
                const token = parts[i];
                // 跳过分隔符或空段
                if (token === '_' || token === '/' || token === '') continue;

                if (Object.prototype.hasOwnProperty.call(dict.word, token)) {
                    const zhToken = dict.word[token];
                    if (typeof zhToken === 'string' && zhToken.length > 0) {
                        parts[i] = zhToken;
                        wordChanged = true;
                        didChange = true;
                    }
                }
            }

            if (wordChanged) {
                currentText = parts.join('');
            }
        }

        // 4) 多义词匹配
        if (dict.multiWord) {
            // 将当前文本按下划线分割成单词数组，用于检测独立的多义词
            const words = currentText.split('_');

            // 遍历所有多义词规则
            ruleLoop: for (const [targetWord, rules] of Object.entries(dict.multiWord)) {
                // 确保 rules 是数组
                const rulesArray = Array.isArray(rules) ? rules : [rules];

                // 在单词数组中查找目标词
                const targetIndex = words.indexOf(targetWord);

                if (targetIndex !== -1) {
                    // 构建前词字符串和后词字符串
                    const beforeStr = words.slice(0, targetIndex).join('_');
                    const afterStr = words.slice(targetIndex + 1).join('_');

                    // 遍历该多义词的所有规则
                    for (const rule of rulesArray) {
                        // 检查多义词匹配条件
                        if (checkMultiWordCondition(beforeStr, afterStr, rule)) {
                            // 应用多义词规则
                            currentText = applyMultiWordRule(beforeStr, afterStr, rule);
                            didChange = true;
                            break ruleLoop; // 只处理第一个匹配的多义词规则
                        }
                    }
                }
            }
        }

        // 应用最终翻译结果
        if (didChange) {
            // 根据配置决定是否去掉下划线
            if (CONFIG.removeUnderscores) {
                currentText = currentText.replace(/_/g, '');
            }
            el.textContent = currentText;
        }
    }

