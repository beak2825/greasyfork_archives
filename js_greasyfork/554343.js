    /**
   * 初始化中文搜索
   */
    function initChineseSearch() {
        const searchBox = document.querySelector('#tags');
        if (!searchBox) return;

        const dropdown = createLoadingDropdown();
        document.body.appendChild(dropdown);

        const reverseIndex = buildReverseIndex();
        let currentController = null;
        let lastChineseSegment = null;

        // 点击页面其他区域关闭下拉框
        document.addEventListener('click', (e) => {
            if (!dropdown.contains(e.target) && e.target !== searchBox) {
                dropdown.style.display = 'none';
            }
        });

        // 防抖搜索函数
        let searchTimeout = null;
        function performSearch() {
            const searchText = searchBox.value;
            const cursorPosition = searchBox.selectionStart;

            // 取消之前的请求
            if (currentController) {
                currentController.abort();
            }

            // 隐藏下拉框
            dropdown.style.display = 'none';

            if (!searchText || !CONFIG.chineseSearchEnabled) {
                return;
            }

            // 获取光标位置的中文部分
            const chineseSegment = getChineseAtCursor(searchText, cursorPosition);

            if (!chineseSegment) {
                return;
            }

            // 保存中文段信息
            lastChineseSegment = chineseSegment;

            // 立即显示加载状态
            showLoading(searchBox, dropdown);

            // 执行匹配
            const allMatches = matchSearchTerm(chineseSegment.text, reverseIndex);

            // 去重并取最相关的几个关键词去请求
            const seen = new Set();
            const keywords = [];

            for (const match of allMatches) {
                if (!seen.has(match.original)) {
                    seen.add(match.original);
                    keywords.push(match.original);

                    // 达到最大并发数时停止
                    if (keywords.length >= CONFIG.maxConcurrent) {
                        break;
                    }
                }
            }

            // 如果去重后数量不足，尝试补位（从剩余匹配中继续取）
            if (keywords.length < CONFIG.maxConcurrent) {
                for (const match of allMatches) {
                    if (keywords.length >= CONFIG.maxConcurrent) break;

                    if (!seen.has(match.original)) {
                        seen.add(match.original);
                        keywords.push(match.original);
                    }
                }
            }

            if (keywords.length > 0) {
                currentController = new AbortController();

                fetchMultipleCandidates(keywords).then(groups => {
                    const allCandidates = [];

                    // 处理网站候选词
                    groups.forEach(group => {
                        const translatedCandidates = translateCandidates(group.candidates, group.keyword);
                        allCandidates.push(...translatedCandidates);
                    });

                    // 显示所有候选词（按帖子数排序）
                    showCandidates(allCandidates, searchBox, dropdown, searchText, chineseSegment);
                }).catch(error => {
                    console.warn('搜索失败:', error);
                    // 错误时显示空状态
                    showCandidates([], searchBox, dropdown, searchText, chineseSegment);
                });
            } else {
                // 没有匹配结果时显示空状态
                showCandidates([], searchBox, dropdown, searchText, chineseSegment);
            }
        }

        // 输入事件监听
        searchBox.addEventListener('input', () => {
            clearTimeout(searchTimeout);
            searchTimeout = setTimeout(performSearch, CONFIG.debounceDelay);
        });

        // 光标移动事件
        searchBox.addEventListener('click', () => {
            clearTimeout(searchTimeout);
            searchTimeout = setTimeout(performSearch, CONFIG.debounceDelay);
        });

        searchBox.addEventListener('keyup', () => {
            clearTimeout(searchTimeout);
            searchTimeout = setTimeout(performSearch, CONFIG.debounceDelay);
        });

        // 焦点事件
        searchBox.addEventListener('focus', () => {
            if (searchBox.value && CONFIG.chineseSearchEnabled) {
                performSearch();
            }
        });
    }

