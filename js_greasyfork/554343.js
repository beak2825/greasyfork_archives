    /**
   * 获取缓存的候选词
   */
    function getCachedCandidates(keyword) {
        const item = candidateCache.get(keyword);
        if (item && Date.now() - item.timestamp < CONFIG.cacheTimeout) {
            return item.data;
        }
        candidateCache.delete(keyword);
        return null;
    }

    /**
   * 设置缓存候选词
   */
    function setCachedCandidates(keyword, data) {
        candidateCache.set(keyword, { data, timestamp: Date.now() });
    }

    /**
   * 从网站获取候选词
   */
    async function fetchTagCandidates(keyword) {
        // 检查缓存
        const cached = getCachedCandidates(keyword);
        if (cached) return cached;

        try {
            const controller = new AbortController();
            const timeoutId = setTimeout(() => controller.abort(), CONFIG.requestTimeout);

            const url = `https://e621.net/tags/autocomplete.json?search[name_matches]=${encodeURIComponent(keyword)}&expiry=7`;
            const response = await fetch(url, { signal: controller.signal });
            clearTimeout(timeoutId);

            if (!response.ok) throw new Error(`HTTP ${response.status}`);

            const data = await response.json();
            const limitedData = data.slice(0, CONFIG.maxCandidatesPerGroup);

            setCachedCandidates(keyword, limitedData);
            return limitedData;
        } catch (error) {
            console.warn(`获取候选词失败 [${keyword}]:`, error);
            return [];
        }
    }

    /**
   * 批量获取候选词
   */
    async function fetchMultipleCandidates(keywords) {
        const results = [];

        for (let i = 0; i < keywords.length; i += CONFIG.maxConcurrent) {
            const batch = keywords.slice(i, i + CONFIG.maxConcurrent);
            const promises = batch.map(keyword =>
                                       fetchTagCandidates(keyword).then(candidates => ({
                keyword,
                candidates
            }))
                                      );

            const batchResults = await Promise.all(promises);
            results.push(...batchResults);

            if (i + CONFIG.maxConcurrent < keywords.length) {
                await new Promise(resolve => setTimeout(resolve, CONFIG.requestInterval));
            }
        }

        return results;
    }

    /**
   * 翻译候选词
   */
    function translateCandidates(candidates, originalKeyword) {
        const seenOriginals = new Set(); // 用于去重
        const uniqueCandidates = [];

        for (const candidate of candidates) {
            if (!seenOriginals.has(candidate.name)) {
                seenOriginals.add(candidate.name);
                uniqueCandidates.push(candidate);
            }
        }

        return uniqueCandidates.map(candidate => {
            const virtualElement = document.createElement('span');
            virtualElement.textContent = candidate.name;

            const category = categoryMapping[candidate.category] || 'general';
            translateOne(virtualElement, category);

            return {
                original: candidate.name,
                translated: virtualElement.textContent,
                category: category,
                postCount: candidate.post_count,
                keyword: originalKeyword
            };
        });
    }

    /**
   * 构建反向索引
   */
    function buildReverseIndex() {
        const reverseIndex = new Map(); // 中文译文 -> {原文, 分类, 类型}

        for (const [category, dict] of Object.entries(categoryDicts)) {
            // 处理complete词表
            if (dict.complete && typeof dict.complete === 'object') {
                for (const [en, zh] of Object.entries(dict.complete)) {
                    if (en && zh && typeof zh === 'string' && zh.length > 0) {
                        if (!reverseIndex.has(zh)) {
                            reverseIndex.set(zh, []);
                        }
                        reverseIndex.get(zh).push({ original: en, category, type: 'complete' });
                    }
                }
            }

            // 处理phrase词表
            if (dict.phrase && typeof dict.phrase === 'object') {
                for (const [en, zh] of Object.entries(dict.phrase)) {
                    if (en && zh && typeof zh === 'string' && zh.length > 0) {
                        if (!reverseIndex.has(zh)) {
                            reverseIndex.set(zh, []);
                        }
                        reverseIndex.get(zh).push({ original: en, category, type: 'phrase' });
                    }
                }
            }

            // 处理word词表
            if (dict.word && typeof dict.word === 'object') {
                for (const [en, zh] of Object.entries(dict.word)) {
                    if (en && zh && typeof zh === 'string' && zh.length > 0) {
                        if (!reverseIndex.has(zh)) {
                            reverseIndex.set(zh, []);
                        }
                        reverseIndex.get(zh).push({ original: en, category, type: 'word' });
                    }
                }
            }
        }

        return reverseIndex;
    }

    /**
   * 匹配搜索词 - 用于决定请求哪些关键词
   */
    function matchSearchTerm(searchTerm, reverseIndex) {
        const allMatches = [];
        const seenOriginals = new Set(); // 用于去重

        // 精确匹配
        if (reverseIndex.has(searchTerm)) {
            const matches = reverseIndex.get(searchTerm);
            for (const match of matches) {
                if (!seenOriginals.has(match.original)) {
                    seenOriginals.add(match.original);
                    allMatches.push({
                        ...match,
                        score: 100, // 精确匹配最高分
                        matchedText: searchTerm
                    });
                }
            }
        }

        // 子串匹配
        for (const [zh, matches] of reverseIndex.entries()) {
            if (zh.includes(searchTerm)) {
                // 计算相关度得分 - 完全包含搜索词得分最高，译文越短得分越高
                let score = 0;

                // 完全匹配得分最高
                if (zh === searchTerm) {
                    score = 100;
                }
                // 子串匹配
                else {
                    score = 80;

                    // 译文越短得分越高
                    score += (1 / zh.length) * 20;

                    // 开头匹配比中间匹配得分高
                    if (zh.startsWith(searchTerm)) {
                        score += 10;
                    }
                }

                for (const match of matches) {
                    if (!seenOriginals.has(match.original)) {
                        seenOriginals.add(match.original);
                        allMatches.push({
                            ...match,
                            score,
                            matchedText: zh
                        });
                    }
                }
            }
        }

        // 拆字匹配（如果子串匹配结果较少）
        if (allMatches.length < CONFIG.maxConcurrent && searchTerm.length > 1) {
            const chars = searchTerm.split('');

            for (const [zh, matches] of reverseIndex.entries()) {
                let matchedChars = 0;
                for (const char of chars) {
                    if (zh.includes(char)) {
                        matchedChars++;
                    }
                }

                if (matchedChars > 0) {
                    // 拆字匹配得分较低，但匹配字数越多得分越高
                    const score = (matchedChars / chars.length) * 30 + (1 / zh.length) * 10;

                    for (const match of matches) {
                        if (!seenOriginals.has(match.original)) {
                            seenOriginals.add(match.original);
                            allMatches.push({
                                ...match,
                                score,
                                matchedText: zh
                            });
                        }
                    }
                }
            }
        }

        // 按得分排序 - 用于选择要请求的关键词
        allMatches.sort((a, b) => {
            // 首先按得分排序
            if (b.score !== a.score) {
                return b.score - a.score;
            }
            // 得分相同则按译文长度排序（短的优先）
            return a.matchedText.length - b.matchedText.length;
        });

        return allMatches;
    }

    /**
   * 提取当前光标位置或选中文本的中文部分
   */
    function getChineseAtCursor(searchText, cursorPosition) {
        // 简单的正则匹配光标位置附近的中文
        const chineseRegex = /[\u4e00-\u9fa5]+/g;
        let match;
        let chineseSegments = [];

        while ((match = chineseRegex.exec(searchText)) !== null) {
            chineseSegments.push({
                text: match[0],
                start: match.index,
                end: match.index + match[0].length
            });
        }

        // 如果没有光标位置信息，返回所有中文
        if (cursorPosition === undefined) {
            return chineseSegments.length > 0 ? chineseSegments[chineseSegments.length - 1] : null;
        }

        // 找到包含光标位置的中文段
        for (const segment of chineseSegments) {
            if (cursorPosition >= segment.start && cursorPosition <= segment.end) {
                return segment;
            }
        }

        // 如果没有找到，返回最近的中文段
        if (chineseSegments.length > 0) {
            return chineseSegments.reduce((nearest, current) => {
                const currentDist = Math.min(
                    Math.abs(cursorPosition - current.start),
                    Math.abs(cursorPosition - current.end)
                );
                const nearestDist = Math.min(
                    Math.abs(cursorPosition - nearest.start),
                    Math.abs(cursorPosition - nearest.end)
                );
                return currentDist < nearestDist ? current : nearest;
            });
        }

        return null;
    }

    /**
   * 替换中文部分为候选词
   */
    function replaceChineseWithTag(searchText, chineseSegment, newTag) {
        const before = searchText.substring(0, chineseSegment.start);
        const after = searchText.substring(chineseSegment.end);

        // 确保左边有空格（除非是开头，或者左侧是特殊符号）
        let leftSpace = '';
        if (before.length > 0) {
            const lastChar = before[before.length - 1];
            // 如果左侧不是特殊符号且不是空格，则添加空格
            if (lastChar !== ' ' && lastChar !== '-' && lastChar !== '~') {
                leftSpace = ' ';
            }
            // 如果左侧是特殊符号，则不添加空格
        }
        // 如果搜索框为空，直接添加（不添加前导空格）

        return before + leftSpace + newTag + after;
    }

    /**
     * 选择候选词
     */
    function selectCandidate(tag, searchBox, originalText, chineseSegment) {
        const newValue = replaceChineseWithTag(originalText, chineseSegment, tag);
        searchBox.value = newValue;

        // 触发输入事件以更新状态
        searchBox.dispatchEvent(new Event('input', { bubbles: true }));
        searchBox.focus();

        // 将光标定位到新插入的tag之后
        const before = originalText.substring(0, chineseSegment.start);
        let addedSpaceLength = 0;
        if (before.length > 0) {
            const lastChar = before[before.length - 1];
            if (lastChar !== ' ' && lastChar !== '-' && lastChar !== '~') {
                addedSpaceLength = 1; // 添加了一个空格
            }
        }

        const cursorPosition = chineseSegment.start + addedSpaceLength + tag.length;

        if (searchBox.setSelectionRange) {
            searchBox.setSelectionRange(cursorPosition, cursorPosition);
        }
    }

