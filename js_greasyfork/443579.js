    // 文字替换函数
    function handleTrans(el) {
        if (el) {
            let newHTML = el.innerHTML
            let matched = false
            for (const chtChar of charMap.keys()) {
                if (newHTML.includes(chtChar)) {
                    matched = true
                    const chsChar = charMap.get(chtChar)
                    newHTML = newHTML.replaceAll(chtChar, chsChar)
                }
            }
            for (const hkWord of wordMap.keys()) {
                if (newHTML.includes(hkWord)) {
                    matched = true
                    const mainlandWord = wordMap.get(hkWord)
                    newHTML = newHTML.replaceAll(hkWord, mainlandWord)
                }
            }
            if (matched) {
                el.innerHTML = newHTML
            }
        }
    }

    // 删除广告
    function removeAd(el) {
        let anchorList = Array.from(el.querySelectorAll('a'))
        anchorList = anchorList.filter(a => {
            for (const adKeyword of adKeywordList) {
                if (a.textContent.includes(adKeyword)) {
                    return true
                }
            }
            return false
        })
        const liList = anchorList.map(a => a.parentNode.parentNode.parentNode.parentNode.parentNode.parentNode.parentNode)
        for (const li of liList) {
            el.removeChild(li)
        }
    }

