function copyCards() { 
  let miniCards = document.querySelectorAll('.card-name a');
  let cardsList = "";
  let untranslated = "";
  for(let i=0; i < miniCards.length; i++) {
    let name = miniCards[i].innerHTML.trim();
    let translation = translations[name];
    if (translation) {
      cardsList = cardsList + translation + ", ";
    } else {
      untranslated = untranslated + name + ", ";
    }
  }
  GM_setClipboard(cardsList);
  if (untranslated == "") {
    alert("Kartenliste erfolgreich in Zwischenablage kopiert.");
  } else {
    alert("Kartenliste teilweise in Zwischenablage kopiert, aber diese Karten konnten nicht übersetzt werden:\n\n" + untranslated);
  }
}

GM_registerMenuCommand("Karten kopieren", copyCards);
