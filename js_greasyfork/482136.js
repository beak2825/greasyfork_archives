function editThreadData(prefix, pin = false, may_lens = true) {
// –ü–æ–ª—É—á–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Ç–µ–º—ã, —Ç–∞–∫ –∫–∞–∫ –æ–Ω –Ω–µ–æ–±—Ö–æ–¥–∏–º –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ
  const threadTitle = $('.p-title-value')[0].lastChild.textContent;
 
  if(pin == false){
    fetch(`${document.URL}edit`, {
      method: 'POST',
      body: getFormData({
      prefix_id: prefix,
      title: threadTitle,
      _xfToken: XF.config.csrf,
      _xfRequestUri: document.URL.split(XF.config.url.fullBase)[1],
      _xfWithData: 1,
      _xfResponseType: 'json',
      }),
    }).then(() => location.reload());
  }
  if(pin == true){
    fetch(`${document.URL}edit`, {
      method: 'POST',
      body: getFormData({
      prefix_id: prefix,
      title: threadTitle,
      discussion_open: 1,
      sticky: 1,
      _xfToken: XF.config.csrf,
      _xfRequestUri: document.URL.split(XF.config.url.fullBase)[1],
      _xfWithData: 1,
      _xfResponseType: 'json',
      }),
    }).then(() => location.reload());
  }
  if(may_lens === true) {
    if(prefix == UNACCEPT_PREFIX || prefix == WATCHED_PREFIX || prefix == CLOSE_PREFIX || prefix == DECIDED_PREFIX) {
      moveThread(prefix, 230); }
 
    if(prefix == WAIT_PREFIX) {
      moveThread(prefix, 917);
    }
  }
}
 
function moveThread(prefix, type) {
// –ü–µ—Ä–µ–º–µ—â–µ–Ω–∏–µ —Ç–µ–º—ã
const threadTitle = $('.p-title-value')[0].lastChild.textContent;
 
fetch(`${document.URL}move`, {
  method: 'POST',
  body: getFormData({
  prefix_id: prefix,
  title: threadTitle,
  target_node_id: type,
  redirect_type: 'none',
  notify_watchers: 1,
  starter_alert: 1,
  starter_alert_reason: "",
  _xfToken: XF.config.csrf,
  _xfRequestUri: document.URL.split(XF.config.url.fullBase)[1],
  _xfWithData: 1,
  _xfResponseType: 'json',
  }),
}).then(() => location.reload());
}
 
function getFormData(data) {
  const formData = new FormData();
  Object.entries(data).forEach(i => formData.append(i[0], i[1]));
  return formData;
  }
