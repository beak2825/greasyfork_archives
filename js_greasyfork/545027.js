    // 动态翻译规则 (使用 Array 存储，因为需要遍历检查)
    const dynamicTranslations = [
        [/^Selected tenants: (\d+)$/, '已选择租户: $1'],
        // 在下方继续添加更多动态翻译规则...
    ];

    const TRANSLATED_MARKER = 'kuma-translated'; // 用于标记已翻译元素的CSS class

    /**
     * 翻译指定DOM节点及其所有子节点中的文本内容。
     * @param {Node} node - 需要被翻译的DOM节点。
     */
    function translateNode(node) {
        // --- 优化2: 避免重复翻译 ---
        // 如果节点本身或其父节点已经被标记为已翻译，则直接跳过
        if (node.closest(`.${TRANSLATED_MARKER}`)) {
            return;
        }

        const walker = document.createTreeWalker(node, NodeFilter.SHOW_TEXT);
        let textNode;
        while (textNode = walker.nextNode()) {
            // 同样，检查父元素是否已被标记，防止在一次遍历中重复处理
            if (textNode.parentElement.closest(`.${TRANSLATED_MARKER}`)) {
                continue;
            }

            const originalText = textNode.nodeValue.trim();
            if (!originalText) continue;

            let translated = false;

            // 1. 首先尝试最高效的静态翻译
            const staticResult = staticTranslations.get(originalText);
            if (staticResult) {
                textNode.nodeValue = textNode.nodeValue.replace(originalText, staticResult);
                translated = true;
            } else {
                // 2. 如果静态翻译失败，再尝试动态(正则)翻译
                for (const [pattern, replacement] of dynamicTranslations) {
                    if (pattern.test(originalText)) {
                        textNode.nodeValue = originalText.replace(pattern, replacement);
                        translated = true;
                        break;
                    }
                }
            }
            
            // 如果发生了翻译，给父元素做上标记
            if (translated) {
                textNode.parentElement.classList.add(TRANSLATED_MARKER);
            }
        }
    }

    // --- 脚本主逻辑 (无需修改) ---

    // 1. 首次加载时翻译
    translateNode(document.body);

    // 2. 使用 MutationObserver 监视动态变化
    const observer = new MutationObserver((mutationsList) => {
        for (const mutation of mutationsList) {
            if (mutation.type === 'childList' && mutation.addedNodes.length > 0) {
                mutation.addedNodes.forEach(newNode => {
                    if (newNode.nodeType === Node.ELEMENT_NODE) {
                        translateNode(newNode);
                    }
                });
            }
        }
    });

    // 3. 启动监视器
    observer.observe(document.body, {
        childList: true,
        subtree: true
    });
