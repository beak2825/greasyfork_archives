    function normalizeQuestion(text) {
        return text.trim()
            .replace(/\s+/g, ' ')
            .replace(/（/g, '(').replace(/）/g, ')')
            .replace(/\s*\(单选题\)\s*\(\d+分\)$/, '')
            .replace(/^判断：\s*/, '');
    }

    function parseAnswerKey(text) {
        const answerMap = new Map();
        const entries = text.split('题目：').filter(entry => entry.trim() !== '');
        for (const entry of entries) {
            const parts = entry.split('正确答案：');
            if (parts.length === 2) {
                const question = normalizeQuestion(parts[0]);
                const answerFull = parts[1].trim();
                const answerLetterMatch = answerFull.match(/^[A-D]/);
                if (question && answerLetterMatch) {
                    answerMap.set(question, answerLetterMatch[0]);
                }
            }
        }
        return answerMap;
    }

    function formatAnswerString(answers) {
        const lines = [];
        const groupSize = 10;
        for (let i = 0; i < answers.length; i += groupSize) {
            const startNum = i + 1;
            const endNum = Math.min(i + groupSize, answers.length);
            const prefix = `${startNum}-${endNum} `.padEnd(8, ' ');
            const group = answers.slice(i, i + groupSize);
            const firstHalf = group.slice(0, 5).join('');
            const secondHalf = group.slice(5).join('');
            const formattedGroup = firstHalf + (secondHalf ? ' ' + secondHalf : '');
            lines.push(prefix + formattedGroup);
        }
        return lines.join('\n');
    }

    // 主函数，实现新的错位答题逻辑
    function findAndAutoSelectAnswers() {
        const answerMap = parseAnswerKey(answerKeyText);
        const allQuestionElements = document.querySelectorAll('#answer-body div[title*="(单选题)"]');

        if (allQuestionElements.length === 0) {
            alert('未在页面上找到题目元素，请确认您在答题页面。');
            return;
        }

        // 步骤 1: 获取完整的、经过特殊规则处理的答案列表
        let finalAnswers = [];
        allQuestionElements.forEach((questionEl) => {
            const questionText = normalizeQuestion(questionEl.getAttribute('title'));
            let foundAnswer = 'X';
            for (const [key, value] of answerMap.entries()) {
                if (key.includes(questionText) || questionText.includes(key)) {
                    foundAnswer = value;
                    break;
                }
            }
            finalAnswers.push(foundAnswer);
        });

        if (finalAnswers.length === 102 && finalAnswers[0] === 'X' && finalAnswers[1] === 'X') {
            finalAnswers = finalAnswers.slice(2);
        } else if (finalAnswers.length === 103 && finalAnswers[0] === 'X' && finalAnswers[1] === 'X' && finalAnswers[2] === 'B') {
            finalAnswers = finalAnswers.slice(3);
        }

        // 步骤 2: 创建用于选择的错位答案列表
        const shiftedAnswers = finalAnswers.slice(1);

        // 步骤 3: 遍历所有题目并使用错位答案进行选择
        allQuestionElements.forEach((questionEl, index) => {
            if (index >= shiftedAnswers.length) {
                console.log(`第 ${index + 1} 题没有对应的错位答案，将不作答。`);
                return;
            }

            const correctAnswerLetter = shiftedAnswers[index];
            const targetLetter = (correctAnswerLetter === 'X') ? 'A' : correctAnswerLetter;

            const optionsContainer = questionEl.parentElement.nextElementSibling;
            if (!optionsContainer) {
                console.error('无法找到选项容器 for:', questionEl.getAttribute('title'));
                return;
            }

            const optionLabels = optionsContainer.querySelectorAll('label.mh-question__radio');
            for (const label of optionLabels) {
                if (label.innerText.trim().startsWith(targetLetter + '.')) {
                    const radioButton = label.querySelector('input[type="radio"]');
                    if (radioButton && !radioButton.checked) {
                        radioButton.click();
                    }
                    break;
                }
            }
        });

        // 步骤 4: 显示完整的原始答案列表以供核对
        const formattedAnswers = formatAnswerString(finalAnswers);
        displayResults(formattedAnswers);
        alert(`自动答题完成！\n\n已为所有 ${allQuestionElements.length} 题选择答案，但使用的是答案列表中的第2-100个答案。\n\n请务必自行核对一遍后提交！`);
    }

