    function getDrawingData(name) {
        return drawingData[name.toUpperCase()] || null;
    }

    // Funcin para dibujar usando los datos
    function drawFromData(name, ctx, offsetX = 0, offsetY = 0, scale = 1) {
        const data = getDrawingData(name);
        if (!data) {
            console.warn(`Datos de dibujo no encontrados para: ${name}`);
            return;
        }

        ctx.beginPath();
        data.forEach(element => {
            if (element.type === 'circle') {
                ctx.arc(
                    element.x1 * scale + offsetX, 
                    element.y1 * scale + offsetY, 
                    element.radius * scale, 
                    0, 
                    2 * Math.PI
                );
            } else {
                // Lnea normal
                ctx.moveTo(element.x1 * scale + offsetX, element.y1 * scale + offsetY);
                ctx.lineTo(element.x2 * scale + offsetX, element.y2 * scale + offsetY);
            }
        });
        ctx.stroke();
    }

