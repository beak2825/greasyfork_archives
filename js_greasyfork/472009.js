var rotationInterval;
var popOutWindow;

function rotateImage() {
    // Choose a random index
    var randomIndex = Math.floor(Math.random() * imageUrls.length);

    var skinInput = document.getElementById("skin");
    skinInput.value = imageUrls[randomIndex];

    // If popOutWindow exists and is not closed, update its content
    if (popOutWindow && !popOutWindow.closed) {
        var skinPreview = popOutWindow.document.getElementById("skinPreview");
        skinPreview.src = imageUrls[randomIndex];

        var skinUrlText = popOutWindow.document.getElementById("skinUrl");
        skinUrlText.innerHTML = imageUrls[randomIndex];
    }

    // Dispatch an 'input' event to notify the game of the change
    var event = new Event('input', {
        bubbles: true,
        cancelable: true,
    });
    skinInput.dispatchEvent(event);
}

function startRotation() {
    // Avoid starting multiple intervals
    if (!rotationInterval) {
        rotationInterval = setInterval(rotateImage, 120000);
        toastr.info("Rotation Started, Skin will change every 2 minutes");
    }
}

function stopRotation() {
    if (rotationInterval) {
        clearInterval(rotationInterval);
        rotationInterval = null;
        toastr.info("Rotation Stopped");
    }
}

