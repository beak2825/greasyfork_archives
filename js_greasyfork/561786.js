function showLabels() {
    document.querySelectorAll('img').forEach(img => {
        const src = img.src || '';
        if (!src.includes('k=')) return;

        // Extract k=... safely and normalize
        let kValue = '';
        try {
            const url = new URL(src, location.href);
            kValue = (url.searchParams.get('k') || '').toUpperCase();
        } catch (e) {
            return;
        }

        if (!kValue) return;

        const adoptedFrom = petData[kValue];
        if (!adoptedFrom) return;

        // Avoid duplicates (more reliable than dataset alone)
        const petCard = img.closest('dl.pet') || img.parentNode;
        if (!petCard) return;
        if (petCard.querySelector('.adopted-from-label')) return;

        const label = document.createElement('div');
        label.className = 'adopted-from-label';
        label.textContent = `Adopted from: ${adoptedFrom}`;
        label.style.fontSize = '12px';
        label.style.marginTop = '2px';

        // Append label in a stable place (dt), not necessarily inside the <a>
        const dt = img.closest('dt') || img.parentNode;
        dt.appendChild(label);

        img.dataset.adoptedFrom = 'true';
        labelElements.push(label);
    });
}

function hideLabels() {
    document.querySelectorAll('.adopted-from-label').forEach(el => el.remove());
    labelElements.length = 0;
    document.querySelectorAll('img').forEach(img => delete img.dataset.adoptedFrom);
}

