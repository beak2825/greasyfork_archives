var textWalker = document.createTreeWalker (
    document.body,
    NodeFilter.SHOW_TEXT,
    null,
    false
);
var textNode = null;

while (textNode = textWalker.nextNode () ) {
    var oldText = textNode.nodeValue;

    for (var M = 0; M < numTerms; M++) {
        oldText = oldText.replace (replaceArray[M][0], replaceArray[M][1]);
    }
    textNode.nodeValue = oldText;
}
