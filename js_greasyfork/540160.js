  function translateChar(c) {
    const uCode = c.charCodeAt(0).toString(16).toUpperCase().padStart(4, '0')
    return mapping[uCode] || c
  }

  function decryptText(text) {
    return [...text].map(translateChar).join('')
  }

  function copyHandler(e) {
    const text = window.getSelection().toString().trim()
    if (!text) return

    window.removeEventListener('copy', copyHandler)

    const decrypted = decryptText(text)

    const textArea = document.createElement('textarea')
    textArea.value = decrypted
    textArea.style.position = 'fixed'
    textArea.style.top = '-1000px'
    document.body.appendChild(textArea)
    textArea.focus()
    textArea.select()

    try {
      e.preventDefault()
      document.execCommand('copy')
      console.log('✅ 解密内容已写入剪贴板:', decrypted)
    } catch (err) {
      console.error('复制失败:', err)
    } finally {
      document.body.removeChild(textArea)
      setTimeout(() => window.addEventListener('copy', copyHandler), 100)
    }
  }

  window.addEventListener('copy', copyHandler)
  console.log('✅ 复制监听脚本已注入')

