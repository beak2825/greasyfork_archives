    function clean(str) { return str.replace(/[^\u4e00-\u9fa5]/g, ""); }

    function solve() {
        const questions = document.querySelectorAll('.questionLi');

        questions.forEach(q => {
            if (q.getAttribute('data-real-filled')) return;

            const stemText = q.querySelector('h3')?.innerText || "";
            if (clean(stemText).length < 5) return;

            let bestKey = null;
            for (let key in DATABASE) {
                if (clean(stemText).includes(clean(key))) {
                    bestKey = key;
                    break;
                }
            }

            if (bestKey) {
                const ans = DATABASE[bestKey];
                const options = q.querySelectorAll('.answerLab, li, .clearfix');
                const map = { 'A': 0, 'B': 1, 'C': 2, 'D': 3, 'E': 4, 'F': 5 };

                [...ans].forEach(char => {
                    const index = map[char.toUpperCase()];
                    let target = options[index];

                    // 优先匹配字母 A. B. C.
                    options.forEach(opt => {
                        if (opt.innerText.trim().match(new RegExp(`^${char}[\\.\\s、\\)]`))) {
                            target = opt;
                        }
                    });

                    if (target) {
                        // 1. 强力视觉提示（粉色背景表示已尝试强制写入数据）
                        target.style.setProperty('background', '#fff0f6', 'important');
                        target.style.setProperty('border', '2px solid #eb2f96', 'important');

                        // 2. 核心：通过原生 jQuery 触发事件 (学习通考试的核心)
                        // 考试页面很多函数挂在 jQuery 对象上
                        const $el = window.jQuery ? window.jQuery(target) : null;
                        const input = target.querySelector('input');

                        if (input) {
                            input.checked = true;
                            // 模拟原生点击
                            input.click();
                            // 触发 jQuery 绑定的所有事件
                            if ($el) {
                                $el.trigger('click');
                                window.jQuery(input).trigger('click').trigger('change');
                            }
                        }

                        // 3. 终极暴力：如果页面有 saveQuestion 函数，尝试直接传参调用
                        // 很多预览页面是通过这个保存的
                        try {
                            const qId = q.getAttribute('data') || q.id.replace(/[^0-9]/g, '');
                            if (window.saveQuestion && qId) {
                                window.saveQuestion(qId);
                            }
                        } catch(e) {}
                    }
                });
                q.setAttribute('data-real-filled', 'true');
            }
        });
    }

    // 扫描频率 3秒，防止请求过快被服务器拒绝
    setInterval(solve, 3000);
