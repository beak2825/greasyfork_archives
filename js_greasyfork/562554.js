    const CACHE_PREFIX = "rive_cn_v1_";

    const LoaderUI = {
        el: null,
        show() {
            if (this.el) return;
            this.el = document.createElement('div');
            Object.assign(this.el.style, {
                position: 'fixed', inset: '0', background: '#090909', color: '#fff',
                zIndex: '9999999', display: 'flex', flexDirection: 'column',
                justifyContent: 'center', alignItems: 'center', fontFamily: 'sans-serif',
                transition: 'opacity 0.4s ease'
            });
            this.el.innerHTML = `
                <div style="font-size:24px;margin-bottom:10px;">Rive 汉化加载中...</div>
                <div id="rive-tm-status" style="font-size:14px;opacity:0.7;">正在检查缓存...</div>
            `;
            document.documentElement.appendChild(this.el);
        },
        log(msg) {
            const statusEl = document.getElementById('rive-tm-status');
            if (statusEl) statusEl.textContent = msg;
            console.log(`[RiveCN] ${msg}`);
        },
        hide() {
            if (!this.el) return;
            this.el.style.opacity = '0';
            setTimeout(() => this.el.remove(), 400);
        }
    };

    const applyPatch = (() => {
        const translationMap = new Map(Object.entries(INTL_DICT));

        const buildPatternReplacements = () => {
            return Array.from(translationMap.entries()).map(([key, translation]) => {
                const escapedKey = key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
                const escapedTranslation = translation
                    .replace(/\\/g, '\\\\')
                    .replace(/"/g, '\\"')
                    .replace(/\n/g, '\\n')
                    .replace(/\r/g, '\\r')
                    .replace(/\t/g, '\\t');

                return {
                    pattern: new RegExp(`"${escapedKey}","([^"]*)"`, 'g'),
                    replacement: `"${key}","${escapedTranslation}"`
                };
            });
        };

        return (code) => {
            console.time('[RiveCN] 汉化耗时');

            const patterns = buildPatternReplacements();
            const marker = '"loading_rive","LOADING RIVE"';
            const markerIndex = code.indexOf(marker);

            if (markerIndex === -1) {
                console.warn('[RiveCN] 未找到翻译数组标记');
                console.timeEnd('[RiveCN] 汉化耗时');
                return code;
            }

            let startPos = -1;
            for (let i = markerIndex; i >= 0; i--) {
                if (code.substring(i, i + 2) === '.K' && code[i + 2] === '(' && code[i + 3] === '[') {
                    startPos = i + 3;
                    break;
                }
                if (i >= 2 && code[i - 2] === '.' && /[A-Z]/.test(code[i - 1]) &&
                    code[i] === '(' && code[i + 1] === '[') {
                    startPos = i + 1;
                    break;
                }
            }

            if (startPos === -1) {
                console.warn('[RiveCN] 未找到数组起始位置');
                console.timeEnd('[RiveCN] 汉化耗时');
                return code;
            }

            let depth = 1, endPos = -1;
            for (let i = startPos + 1; i < code.length && depth > 0; i++) {
                if (code[i] === '[') depth++;
                else if (code[i] === ']') {
                    depth--;
                    if (depth === 0) {
                        endPos = i;
                        break;
                    }
                }
            }

            if (endPos === -1) {
                console.warn('[RiveCN] 未找到数组结束位置');
                console.timeEnd('[RiveCN] 汉化耗时');
                return code;
            }

            let arrayContent = code.substring(startPos + 1, endPos);
            let matchCount = 0;

            for (const { pattern, replacement } of patterns) {
                arrayContent = arrayContent.replace(pattern, () => {
                    matchCount++;
                    return replacement;
                });
            }

            const result = code.substring(0, startPos + 1) + arrayContent + code.substring(endPos);

            console.log(`[RiveCN] 汉化完成: ${matchCount}/${translationMap.size} 条`);
            console.timeEnd('[RiveCN] 汉化耗时');
            return result;
        };
    })();

    const executePatchedCode = (code) => new Promise((resolve, reject) => {
        try {
            const blob = new Blob([code], { type: 'text/javascript' });
            const url = URL.createObjectURL(blob);
            const script = Object.assign(document.createElement('script'), {
                src: url,
                onload: () => {
                    URL.revokeObjectURL(url);
                    resolve();
                },
                onerror: (e) => {
                    URL.revokeObjectURL(url);
                    console.error('[RiveCN] 脚本加载失败:', e);
                    reject(e);
                }
            });
            document.head.appendChild(script);
        } catch (e) {
            console.error('[RiveCN] 创建脚本失败:', e);
            reject(e);
        }
    });

    const processMainJs = async (url) => {
        const cacheKey = CACHE_PREFIX + url;
        const cached = GM_getValue(cacheKey);

        if (cached) {
            LoaderUI.log('使用本地缓存...');
            await new Promise(r => setTimeout(r, 50));
            return cached;
        }

        LoaderUI.log('拦截文件中...');
        return new Promise((resolve, reject) => {
            GM_xmlhttpRequest({
                method: "GET",
                url: url.startsWith('http') ? url : location.origin + url,
                onload: ({ status, responseText }) => {
                    if (status !== 200) return reject(`拦截文件失败: ${status}`);

                    LoaderUI.log('正在汉化...');
                    setTimeout(() => {
                        const patched = applyPatch(responseText);
                        try {
                            GM_setValue(cacheKey, patched);
                            LoaderUI.log('缓存已保存');
                            GM_listValues()
                                .filter(k => k.startsWith(CACHE_PREFIX) && k !== cacheKey)
                                .forEach(k => GM_deleteValue(k));
                        } catch (e) {
                            console.warn('[RiveCN] 缓存操作失败:', e.message);
                        }
                        resolve(patched);
                    }, 10);
                },
                onerror: reject
            });
        });
    };

    const proxyFlutterLoader = () => {
        unsafeWindow._flutter ??= {};

        unsafeWindow._flutter.loader = {
            async loadEntrypoint(config) {
                LoaderUI.show();

                unsafeWindow._rive_pending_config = config;

                try {
                    const code = await processMainJs(config.entrypointUrl);
                    LoaderUI.log('注入汉化代码...');
                    await executePatchedCode(code);
                } catch (e) {
                    console.error('[RiveCN] 错误:', e);
                    alert('汉化脚本加载失败，请检查控制台。');
                    LoaderUI.hide();
                }
            },

            async didCreateEngineInitializer(engineInitializer) {
                const config = unsafeWindow._rive_pending_config;
                if (config?.onEntrypointLoaded) {
                    LoaderUI.log('启动 Rive 引擎...');
                    try {
                        await config.onEntrypointLoaded(engineInitializer);
                        setTimeout(() => LoaderUI.hide(), 500);
                    } catch (e) {
                        console.error('[RiveCN] 引擎启动失败:', e);
                        LoaderUI.hide();
                    }
                }
            }
        };
    };

    proxyFlutterLoader();

    GM_registerMenuCommand("清除汉化缓存", () => {
        try {
            const keys = GM_listValues().filter(k => k.startsWith(CACHE_PREFIX));
            keys.forEach(k => GM_deleteValue(k));
            alert(`已清除 ${keys.length} 个缓存，请刷新页面。`);
            console.log(`[RiveCN] 已清除 ${keys.length} 个缓存`);
        } catch (e) {
            console.error('[RiveCN] 清除缓存失败:', e);
            alert(`清除缓存失败: ${e.message}`);
        }
    });

