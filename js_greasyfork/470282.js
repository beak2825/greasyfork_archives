    function createLookupTable(data) {
          var lookupTable = {};
          var resultColumn = "daan";
          for (var i = 1; i < data.length; i++) {
                var row = data[i];
                var timi = row[3];
                var leixing = row[2];
                var result = row[data[0].indexOf(resultColumn)];

                if (!lookupTable[timi]) {
                    lookupTable[timi] = {};
                }

                if (!lookupTable[timi][leixing]) {
                    lookupTable[timi][leixing] = result;
                }
          }
          return lookupTable;
    }

